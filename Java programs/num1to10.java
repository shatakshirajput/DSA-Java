public class num1to10 {
    public static void main(String[] args) {
        for (int i=1;i<=10;i++){
            System.out.println(i);
        }
        // second approach
        int a = 1;
        while(a<11){
            System.out.println(a);
            a++;
        }
        // Third approach
        int b = 1;
        do{
            System.out.println(b);
            b++;
        }while(b<11);
    }
}
